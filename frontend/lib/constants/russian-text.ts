// Helper function to get nested text
export function getText(path: string): string {
  const keys = path.split('.')
  let current: any = RUSSIAN_TEXT
  
  for (const key of keys) {
    if (current && typeof current === 'object' && key in current) {
      current = current[key]
    } else {
      return path // Return the path if not found
    }
  }
  
  return typeof current === 'string' ? current : path
}

// Helper function for pluralization in Russian
export function pluralize(count: number, forms: [string, string, string]): string {
  const n = Math.abs(count) % 100
  const n1 = n % 10
  
  if (n > 10 && n < 20) return forms[2]
  if (n1 > 1 && n1 < 5) return forms[1]
  if (n1 === 1) return forms[0]
  return forms[2]
}

// Helper function for time formatting
export function formatTimeAgo(date: Date): string {
  const now = new Date()
  const diffMs = now.getTime() - date.getTime()
  const diffMinutes = Math.floor(diffMs / (1000 * 60))
  const diffHours = Math.floor(diffMs / (1000 * 60 * 60))
  const diffDays = Math.floor(diffMs / (1000 * 60 * 60 * 24))

  if (diffMinutes < 1) return RUSSIAN_TEXT.time.now
  if (diffMinutes < 60) return `${diffMinutes} ${pluralize(diffMinutes, ['–º–∏–Ω—É—Ç—É', '–º–∏–Ω—É—Ç—ã', '–º–∏–Ω—É—Ç'])} ${RUSSIAN_TEXT.time.ago}`
  if (diffHours < 24) return `${diffHours} ${pluralize(diffHours, ['—á–∞—Å', '—á–∞—Å–∞', '—á–∞—Å–æ–≤'])} ${RUSSIAN_TEXT.time.ago}`
  if (diffDays < 7) return `${diffDays} ${pluralize(diffDays, ['–¥–µ–Ω—å', '–¥–Ω—è', '–¥–Ω–µ–π'])} ${RUSSIAN_TEXT.time.ago}`
  
  return date.toLocaleDateString('ru-RU')
}